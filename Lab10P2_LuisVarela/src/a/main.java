    private void ListoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ListoActionPerformed
        Hilopantalla hp;
        if (i_usuario.getText().equals("admin") && i_contrasena.getText().equals("admin")) {
            MenuAdmin.setVisible(true);
            iniciar_crear.setVisible(false);
        } else if (i_usuario.getText() != null && i_contrasena.getText() != null) {
            AdminJugador j = new AdminJugador("./Usuario.usr");
            j.cargarArchivo();
            boolean usuario_no_encontrado = true;
            int num1 = 0;
            int num2 = 0;
            for (jugador object : j.getLista()) {
                if (i_usuario.getText().equals(object.getUsuario()) && i_contrasena.getText().equals(object.getContrase침a())) {
                    num1 = j.getLista().indexOf(object);
                    num2 = i_contrasena.getText().length();
                    usuario_no_encontrado = false;
                    usu.setText("" + i_usuario.getText());
                    usu.setVisible(false);
                    contra.setVisible(false);
                    contra.setText("" + i_contrasena.getText());
                }
            }
            num1++;
            hp = new Hilopantalla(PantalladeCarga, MenuUsuario, num1, num2, barra1, barra2);
            if (!usuario_no_encontrado) {
                iniciar_crear.setVisible(false);
                PantalladeCarga.setVisible(true);
                barra2.setVisible(false);
                hp.start();
            }
        }
        i_usuario.setText("");
        i_contrasena.setText("");
    }//GEN-LAST:event_ListoActionPerformed

