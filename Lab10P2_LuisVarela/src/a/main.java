    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        String marca = "" + pais_c.getSelectedItem();
        String modelo = "" + marca_c.getSelectedItem();
        String pais = "" + modelo_c.getSelectedItem();
        Color color = carro_color.getBackground();
        double precio = Double.parseDouble(precio_carro.getText());
        Date fecha = Fecha.getDate();
        int Horsepower = Integer.parseInt(caballero_fuerza.getText());
        int VelocidadP = Integer.parseInt(velocidad_punta.getText());
        boolean reconstruido = Reconstruido.isSelected();
        int tiempo = 0;
        if (cb_tiempo.getSelectedIndex() == 0) {
            tiempo = 60;
        } else if (cb_tiempo.getSelectedIndex() == 1) {
            tiempo = 100;
        }
        AdminCarro AC = new AdminCarro("./Carros.car");
        AC.cargarArchivo();
        carro temp = new carro(marca, modelo, color, precio, pais, fecha, reconstruido, Horsepower, VelocidadP, tiempo);
        AC.agregar(temp);
        AC.escribirArchivo();
        AdminConcesionaria AdC = new AdminConcesionaria("./Consesionaria.cns");
        AdC.cargarArchivo();
        ArrayList<concesionaria> con = AdC.getLista();
        con.get(cb_concesionaria.getSelectedIndex()).getC().add(temp);
        AdC.setlista(con);
        AdC.escribirArchivo();
        precio_carro.setText("");
        caballero_fuerza.setText("");
        velocidad_punta.setText("");
    }//GEN-LAST:event_jButton5ActionPerformed

